// Operador ternario
// (condicion) ? true : false

// (condicion) ?
//     (primera sentencia,
//     segunda sentencia) :
//     (primera sentencia,
//     segunda sentencia)

let num = 33;
console.log(num);
console.log(num % 2 == 0);

(num % 2 == 0) ? console.log(`${num} es par`) : console.log(`${num} no es par`);
(num % 3 == 0) ? console.log(`${num} es multiplo de tres`) : console.log(`${num} no es multiplo de tres`);